%    TEMP  IN CENT.                                                     
%    E     IN PA                                                        
% ############################################################
                                      
     A0=6.107799961;
     A1=4.436518521E-1;
     A2=1.428945805E-2;
     A3=2.650648471E-4;
     A4=3.031240396E-6;
     A5=2.034080948E-8;
     A6=6.136820929E-11;
