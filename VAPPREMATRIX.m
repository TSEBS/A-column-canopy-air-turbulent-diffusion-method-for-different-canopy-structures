     rr=T>=0;     
     E(rr)=A0+T(rr).*(A1+T(rr).*(A2+T(rr).*(A3+T(rr).*(A4+T(rr).*(A5+T(rr)*A6)))));  
     E=E.*100;                                                   
                                                                 
                                     